const int P = 998244353;//模数
using mint = MInt<P>;

typedef pair<mint,mint> PII;

PII operator+(PII a, PII b)
{
    return {a.first*b.first,a.first*b.second+a.second};
}

mint a[N],b[N];
PII f[N][21];


PII ask(int l,int r){
    PII res = {1,0};
    int k = r-l+1;
    for(int i=0;i<20;i++){
        if(k>>i & 1) {
            res = res + f[l][i];
            l += (1<<i);
        }
    }
    return res;
    //cout<<l<<" "<<r<<endl;
}

void solve()
{
    int n;
    cin>>n;
    for(int i=1;i<=n;i++)cin>>a[i];
    for(int i=1;i<=n;i++)cin>>b[i];

    for(int i=0;i<20;i++){
        for(int j=1;j + (1 << i) - 1 <= n;j++){
            if(i==0)f[j][i] = {a[j],b[j]};
            else f[j][i] = f[j][i-1] + f[j + (1 << i-1)][i-1];
        }
    }

    int q;cin>>q;
    while(q--) {
        int l,r;
        mint y;
        cin>>l>>r>>y;
        PII res = ask(l,r);
       // cout<<res.first<<" "<<res.second<<endl;

        if (res.first == 0) {
            if(res.second==y)cout<<"inf\n";
            else cout<<"-1\n";
        } else 
        cout<< (y - res.second)/res.first<<endl;
    }
}


